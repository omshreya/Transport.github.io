package com.transfer.model;

public class Extra 
{
	private int Food;
	private int Stay;
	
	public Extra()
	{
		
	}

	public int getFood() 
	{
		return Food;
	}

	public void setFood(int food) 
	{
		Food = food;
	}

	public int getStay() 
	{
		return Stay;
	}

	public void setStay(int stay) 
	{
		Stay = stay;
	}
	
	
}
